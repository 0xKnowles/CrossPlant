inline const uint32_t* getSpriteRows(PetStage stage, uint8_t variant, uint8_t petType, uint8_t frame) {
  (void)variant; // variant unused in custom plant artwork
  if (frame > 2) frame = 0;
  if (stage == PetStage::DEAD) return kSprite_Withered[frame];

  int idx;
  switch (stage) {
    case PetStage::EGG:       idx = 0; break;
    case PetStage::HATCHLING: idx = 1; break;
    case PetStage::YOUNGSTER: idx = 2; break;
    case PetStage::COMPANION: idx = 3; break;
    case PetStage::ELDER:     idx = 4; break;
    default:                  return kSprite_Withered[frame];
  }

  switch (petType) {
    case 0: return kMonstera[idx][frame];
    case 1: return kBegonia[idx][frame];
    case 2: return kAlocasia[idx][frame];
    default: return kMonstera[idx][frame];
  }
}

